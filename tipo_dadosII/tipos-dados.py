# Tipo de dados #
print(type(3))
print(type(3.0))
print(type('Olá'))
print(type([1, 2, 3]))
print(type(True))


# Criando um objeto #
a = 3
b = int(3)
c = 3.0
d = float(3.0)
e = 'text'
f = str('text')
g = [1, 2, 'ab', []]
h = list([1, 2, 'ab', []])
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
print(type(h))

